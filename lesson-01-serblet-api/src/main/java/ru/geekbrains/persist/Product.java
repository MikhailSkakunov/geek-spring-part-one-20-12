package ru.geekbrains.persist;

public class Product {
    private String name;
    private Long id;

    public Product() {
    }
    public Product(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
